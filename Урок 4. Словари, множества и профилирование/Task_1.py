# Даны два неупорядоченных набора целых чисел (может быть, с повторениями). 
# Выдать без повторений в порядке возрастания все те числа, 
# которые встречаются в обоих наборах.
# Пользователь вводит 2 числа. n - кол-во элементов первого множества. m - кол-во элементов второго множества. Затем пользователь вводит сами элементы множеств.
# 11 6
# 2 4 6 8 10 12 10 8 6 4 2
# 3 6 9 12 15 18
# 6 12


# Программа для вывода чисел, которые встречаются в обоих множествах

# Ввод количества элементов в первом и втором множествах
print("Введите количество элементов в первом множестве:")
n = int(input())

print("Введите количество элементов во втором множестве:")
m = int(input())

# Ввод элементов первого множества
print("Введите элементы первого множества, разделяя их пробелами:")
set1 = set(map(int, input().split()))

# Ввод элементов второго множества
print("Введите элементы второго множества, разделяя их пробелами:")
set2 = set(map(int, input().split()))

# Вычисление пересечения множеств и сортировка результата
result = sorted(set1 & set2)

# Вывод результата
print("Числа, которые встречаются в обоих множествах:")
print(*result)